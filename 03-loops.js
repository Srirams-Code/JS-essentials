// loops

// 1. for loop
//  Syntax:
//  for(initialization; condition; increment/decrement) {
//      // code
//  }

//  Example:

for (let i = 0; i < 5; i++) {
    console.log(i);
}

// 2. while loop
//  Syntax:

//  while(condition) {
//      // code
//  }

//  Example:

let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}